package com.aspire.java.conditionalStatement;

public class If2 {
	public static void main(String[] args) {
		
	
	int a=8, b=3, c=5;
	
	if(a>b&&a>c)
	{
		System.out.println("both conditions are true");
	}
	if(a>b||a>c)
	{
		System.out.println("Either of condition is true");
	}

}
}