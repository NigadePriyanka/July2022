//123
//1234
//12345
//rows=5; count=5
public class Num5 

{
public static void main(String[] args) 
{
	int rows=1;
	for(rows=1; rows<=1; rows++)
	{
		int count=1;
		for(count=1; count<=rows; count++)
		{
			System.out.print(count);
		}
		System.out.println();
	}
}
}
